public class Chalja {
	private String aman;
	private String amankiphoto;
	private String amankaemail;

	public Chalja(String aman, String amankiphoto, String amankaemail)
	{
		this.aman = aman;
		this.amankiphoto = amankiphoto;
		this.amankaemail = amankaemail;

	}
	public Chalja()
	{

	}
	public String getAman()
	{
		return aman;
	}
	public  String getAmankiphoto()
	{
		return amankiphoto;
	}
	public String getAmankaemail()
	{
		return amankaemail;
	}


}
